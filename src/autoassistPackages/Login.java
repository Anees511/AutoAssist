    private void btnloginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnloginActionPerformed
        // TODO add your handling code here:
        if (txtUserName.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(Login.this, "Please Enter User Name");
            txtUserName.grabFocus();
            return;
        } else if (txtPassword.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(Login.this, "Please Enter Password");
            txtPassword.grabFocus();
            return;
        }
         String userName = txtUserName.getText().trim();
         String password = new String(txtPassword.getPassword()); // Assuming txtPassword is a JPasswordField

        boolean isAuthenticated = false;

        // Check in userinfo database
        try (Connection conn = DriverManager.getConnection(DB_USERINFO_URL, DB_USER, DB_PASSWORD)) {
            if (checkCredentials(conn, "records", userName, password)) {
                isAuthenticated = true;
                
                Login.this.dispose();
                
                 new UserDashboard(userName).setVisible(true);
                 JOptionPane.showMessageDialog(Login.this, "Login Successful");
                
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(Login.this, "Database connection error (userinfo)");
        }

        // Check in workerinfo database if not authenticated yet
        if (!isAuthenticated) {
            try (Connection conn = DriverManager.getConnection(DB_WORKERINFO_URL, DB_USER, DB_PASSWORD)) {
                if (checkCredentials(conn, "workerrecord", userName, password)) {
                    isAuthenticated = true;
                    
                    Login.this.dispose();
                    new WorkerDashboard(userName).setVisible(true);
                    JOptionPane.showMessageDialog(Login.this, "Login Successful");
                }
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(Login.this, "Database connection error (workerinfo)");
            }
        }

        if (!isAuthenticated) {
            JOptionPane.showMessageDialog(Login.this, "Invalid username or password");
            txtPassword.setText("");
            txtPassword.grabFocus();
        }       
    }//GEN-LAST:event_btnloginActionPerformed

