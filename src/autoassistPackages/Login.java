private boolean checkCredentials(Connection conn, String tableName, String userName, String password) throws SQLException {
        String query = "SELECT * FROM " + tableName + " WHERE FullName = ? AND Password = ?";
        try (PreparedStatement stmt = conn.prepareStatement(query)) {
            stmt.setString(1, userName);
            stmt.setString(2, password);
            try (ResultSet rs = stmt.executeQuery()) {
                return rs.next();
            }
        }
    }

